#include<stdio.h>
 int main()
 {
     int a=10,b=56;
     double c=10.2,d=54.325;
     float e=25.67,f=98.3515;
     long int g=564545415,h=6451232;

     printf("integer value\ns are: %d   %d\n",a,b);
     printf("Double values are: %lf   %lf\n",c,d);
     printf("Float values are: %f   %f\n",e,f);
     printf("Long integer values are: %ld   %ld\n",g,h);

     return 0;
}
